        public IdCardInfo Parse(out string errMsg)
        {
            int length = IdcardNo.Length;
            if (length == 18) { return Parse18(out errMsg); }
            else if (length == 15) { return Parse15(out errMsg); }
            else
            {
                errMsg = "身份证号码位数不对";
                return null;
            }
        }
        public IdCardInfo Parse18(out string errMsg)
        {
            string Id = IdcardNo;
            errMsg = string.Empty;
            long n = 0;

            //长度和数值特性
            if (long.TryParse(Id.Remove(17), out n) == false  //  前17位是数字
                || n < Math.Pow(10, 16)   //前17位的数字值大于 10的16次方
                || long.TryParse(Id.Replace('x', '0').Replace('X', '0'), out n) == false)//将最后一位的x替换成0
            {
                errMsg = "身份证号码格式有误";
            }
            //地址
            // 1-2位省、自治区、直辖市代码；  
            int provinceCode = Convert.ToInt32(Id.Remove(2));
            int cityCode = Convert.ToInt32(Id.Remove(4)+"00");

            int countryCode = Convert.ToInt32(Id.Remove(6));
            if (!ProvinceDict.ContainsKey(provinceCode))
            {
                errMsg = "身份证号码省份不正确";
                //省份验证
                return null;
            }
            this.ProvinceCode = provinceCode;
            if (!CityDict.ContainsKey(cityCode))
            {

                errMsg = "身份证号码城市代码不正确";
                //城市
                return null;
            }
            if (!CityDict.ContainsKey(countryCode))
            {
                errMsg = "身份证号码城市代码不正确";
                //城市
                return null;
            }

            this.CityCode = cityCode;
            this.CountryCode = countryCode;

            string birth = Id.Substring(6, 8).Insert(6, "-").Insert(4, "-");
            DateTime birthday;
            if (DateTime.TryParse(birth, out birthday) == false)
            {
                errMsg = "身份证号码生日不正确";
                return null;
            }
            this.Age = DateTime.Now.Year - birthday.Year+1;
            //性别
            int sexCode = Convert.ToInt32(Id.Substring(16, 1)) % 2;
            this.Sex = sexCode == 0 ? enumSex.女 : enumSex.男;
            //---校验码验证
            string[] arrVarifyCode = ("1,0,x,9,8,7,6,5,4,3,2").Split(',');
            string[] Wi = ("7,9,10,5,8,4,2,1,6,3,7,9,10,5,8,4,2").Split(',');
            char[] Ai = Id.Remove(17).ToCharArray();
            int sum = 0;
            for (int i = 0; i < 17; i++)
            {
                sum += int.Parse(Wi[i]) * int.Parse(Ai[i].ToString());
            }
            int y = -1;
            Math.DivRem(sum, 11, out y);
            if (arrVarifyCode[y] != Id.Substring(17, 1).ToLower())
            {
                errMsg = "身份证号码校验码不正确";
                return null;
            }
            return this;
        }
        public IdCardInfo Parse15(out string errMsg)
        {
            string Id = IdcardNo;
            errMsg = string.Empty;
            long n = 0;
            if (long.TryParse(Id, out n) == false || n < Math.Pow(10, 14))
            {
                errMsg = "身份证号码格式有误";
                return null;//数字验证
            }
            int provinceCode = Convert.ToInt32(Id.Remove(2));
            int cityCode = Convert.ToInt32(Id.Remove(4)+"00");
            int countryCode = Convert.ToInt32(Id.Remove(6));
            if (!ProvinceDict.ContainsKey(provinceCode))
            {
                errMsg = "身份证号码省份不正确";
                //省份验证
                return null;
            }
            this.ProvinceCode = provinceCode;
            if (!CityDict.ContainsKey(cityCode))
            {

                errMsg = "身份证号码城市代码不正确";
                //城市
                return null;
            }
            if (!CityDict.ContainsKey(countryCode))
            {

                errMsg = "身份证号码区县代码不正确";
                //城市
                return null;
            }
            this.CityCode = cityCode;
            this.CountryCode = countryCode;
            string birth = Id.Substring(6, 6).Insert(4, "-").Insert(2, "-");
            DateTime birtyday;
            if (DateTime.TryParse(birth, out birtyday) == false)
            {
                errMsg = "身份证号码生日不正确";
                return null;//生日验证
            }
            this.Age =DateTime.Now.Year- birtyday.Year+1;
            return this;//符合15位身份证标准
        }

